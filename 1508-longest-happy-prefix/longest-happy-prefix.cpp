class Solution {
public:
    string longestPrefix(string s) {
        int n = s.size();
        
        vector<int> lps(n, 0);


        for (int i = 1, j = 0; i < n; i++) {
            while (j > 0 && s[i] != s[j]) j = lps[j - 1];
            if (s[i] == s[j]) j++;
            lps[i] = j;
        }


        return s.substr(0, lps[n - 1]);
        }
};
